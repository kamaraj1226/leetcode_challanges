def solution(grid):

    n = len(grid)
    for i in range(1, n):
        for j in range(n):
            min_sum = float("inf")
            for x in range(n):
                if x == j:
                    continue
                min_sum = min(grid[i - 1][x] + grid[i][j], min_sum)
            grid[i][j] = min_sum

    return min(grid[-1])


def optimized_solution(grid):

    def two_mins(l):
        d = {x: (i, x) for i, x in enumerate(l)}
        if len(d) == 1:
            _key = list(d.keys())[0]
            return (0, _key), (1, _key)

        first, second = sorted(d)[:2]
        first = d.get(first)
        second = d.get(second)
        return first, second

    n = len(grid)

    for i in range(1, n):
        first, second = two_mins(grid[i - 1])

        fp, fv = first
        sp, sv = second
        for j in range(n):

            if j == fp:
                grid[i][j] = grid[i][j] + sv

            else:
                grid[i][j] = grid[i][j] + fv

    return min(grid[-1])


