def solution(land):
    from collections import deque

    row_end = len(land)
    col_end = len(land[0])
    groups = []

    for i, r in enumerate(land):
        for j, c in enumerate(r):
            if c == 0:
                continue

            queue = deque()
            queue.append((i, j))
            _sr, _sc, _er, _ec = i, j, i, j

            while len(queue) > 0:
                _r, _c = queue.pop()
                land[_r][_c] = 0

                # down
                if (
                    0 <= _r + 1 < row_end
                    and 0 <= _c < col_end
                    and land[_r + 1][_c] == 1
                ):
                    queue.append((_r + 1, _c))
                    _sr, _sc, _er, _ec = (
                        min(_r + 1, _sr),
                        min(_c, _sc),
                        max(_r + 1, _er),
                        max(_c, _ec),
                    )

                # right
                if (
                    0 <= _r < row_end
                    and 0 <= _c + 1 < col_end
                    and land[_r][_c + 1] == 1
                ):
                    queue.append((_r, _c + 1))
                    _sr, _sc, _er, _ec = (
                        min(_r, _sr),
                        min(_c + 1, _sc),
                        max(_r, _er),
                        max(_c + 1, _ec),
                    )
            # print(_sr, _sc, _er, _ec)
            groups.append([_sr, _sc, _er, _ec])
    return groups


